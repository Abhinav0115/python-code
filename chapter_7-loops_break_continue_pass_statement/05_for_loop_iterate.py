# for loop with list
l = [10, 20, 30, 40, 50]
for i in l:
    print(i)
# Output: 10 20 30 40 50

print("-------------------------")

# for loop with tuple
t = (10, 20, 30, 40, 50)
for i in t:
    print(i)
# Output: 10 20 30 40 50

print("-------------------------")

# for loop with string
s = "Python"
for i in s:
    print(i)
# Output: P y t h o n

print("-------------------------")

# for loop with set
s = {10, 20, 30, 40, 50}
for i in s:
    print(i)
# Output: 40 10 50 20 30

print("-------------------------")

# for loop with dictionary
d = {1: 'one', 2: 'two', 3: 'three'}
for i in d:
    print(i)
# Output: 1 2 3

print("-------------------------")

# for loop with dictionary
d = {1: 'one', 2: 'two', 3: 'three'}
for i in d.values():
    print(i)
# Output: one two three

print("-------------------------")

# for loop with dictionary
d = {1: 'one', 2: 'two', 3: 'three'}
for i in d.items():
    print(i)
# Output: (1, 'one') (2, 'two') (3, 'three')

print("-------------------------")

# for loop with dictionary
d = {1: 'one', 2: 'two', 3: 'three'}
for key, value in d.items():
    print(key, value)
# Output: 1 one 2 two 3 three

print("-------------------------")

# for loop with dictionary
d = {1: 'one', 2: 'two', 3: 'three'}
for key in d.keys():
    print(key)
# Output: 1 2 3

print("-------------------------")

